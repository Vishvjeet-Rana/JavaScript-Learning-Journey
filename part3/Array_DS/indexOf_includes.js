let colors = ["red", "green", "blue", "violet", "pink"];

// indexOf gives index of ele if present otherwise gives -1
console.log(colors.indexOf("blue")); //2
console.log(colors.indexOf("pink")); //4
console.log(colors.indexOf("yellow")); // -1

// includes method checks if the entered ele presents in array or not
console.log(colors.includes("yellow")); //false
console.log(colors.includes("violet")); // true
